def rgb_loader(path, dataset_root):
    absolute_path = os.path.join(dataset_root, path)
    with open(absolute_path, 'rb') as f:
        with Image.open(f) as img:
            return img.convert('RGB')

def l_loader(path):
    with open(path, 'rb') as f:
        with Image.open(f) as img:
            return img.convert('L')

